package function.choose;

public class AreaVO {
	public String area;

	public AreaVO(String area) {
		super();
		this.area = area;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	@Override
	public String toString() {
		return "AreaVO [area=" + area + "]";
	}
	
}
